// Use typeof para verificar o tipo de uma variável

// Você pode usar typeof para verificar a estrutura de dados, ou tipo, de uma variável.

// O JavaScript reconhece sete tipos de dados primitivos (imutáveis): Boolean, Null, Undefined, Number, String, Symbol (novo com ES6) e BigInt (novo com ES2020) e um tipo para itens mutáveis: Object.

// Adicione duas instruções console.log() para verificar o tipo de cada uma das duas variáveis ​​sete e três no código.

